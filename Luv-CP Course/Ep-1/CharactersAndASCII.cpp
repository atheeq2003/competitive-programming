// Characters and ASCII
#include<bits/stdc++.h>
using namespace std;
int main(){
    char a = 'c';            
    char b = 'd';
    cout << a << endl;
    cout << b << endl;
    cout << (int) a << endl; // Use int to print ascii value of an charater
    cout << (int) b << endl;
    cout << a - b;           // This prints the difference values of two characters which are ascii values 
}